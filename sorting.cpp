 /*
  * ============================================================================
  *                         ЧАСТЬ 1: БЫСТРАЯ СОРТИРОВКА (QUICKSORT)
  * ============================================================================
  *
  * Быстрая сортировка - один из самых эффективных алгоритмов сортировки.
  * Средняя сложность: O(n log n), худшая: O(n²).
  *
  * Принцип работы:
  * 1. Выбирается опорный элемент (pivot)
  * 2. Массив разбивается на две части: элементы меньше опорного и больше
  * 3. Рекурсивно сортируются обе части
  *
  * Улучшения в реализации:
  * - Опорный элемент выбирается как средний элемент массива
  * - Используется двухстороннее разбиение (указатели движутся навстречу)
  * - Это предотвращает деградацию на отсортированных данных
  * ============================================================================
  */

  /*
   * Разбиение массива для быстрой сортировки
   *
   * Параметры:
   *   arr[] - массив для разбиения
   *   low   - индекс первого элемента подмассива
   *   high  - индекс последнего элемента подмассива
   *
   * Возвращает: индекс, разделяющий массив на две части
   *
   * Алгоритм:
   * 1. Выбираем опорный элемент как средний элемент a[(l+r)/2]
   * 2. Двигаем указатель i слева направо, пока a[i] < опорный
   * 3. Двигаем указатель j справа налево, пока a[j] > опорный
   * 4. Если i >= j - останавливаемся
   * 5. Меняем местами a[i] и a[j], затем i++, j--
   * 6. Повторяем шаги 2-5 пока i <= j
   * 7. Возвращаем индекс j
   */
static int partition(int arr[], int low, int high) {
    int v = arr[(low + high) / 2];   /* Опорный элемент — средний элемент массива */
    int i = low;                     /* Левый указатель - начинает с первого элемента */
    int j = high;                    /* Правый указатель - начинает с последнего элемента */
    int temp;                        /* Временная переменная для обмена элементов */

    /* Основной цикл разбиения - выполняется пока указатели не пересеклись */
    while (i <= j) {
        /* Двигаем левый указатель вправо, пока элемент меньше опорного */
        while (arr[i] < v) {
            i++;
        }

        /* Двигаем правый указатель влево, пока элемент больше опорного */
        while (arr[j] > v) {
            j--;
        }

        /* Если указатели встретились или пересеклись - разбиение завершено */
        if (i >= j) {
            break;
        }

        /* Меняем местами элементы, которые находятся не на своих местах */
        temp = arr[i];
        arr[i] = arr[j];
        arr[j] = temp;

        /* Сдвигаем указатели к центру после обмена */
        i++;
        j--;
    }

    return j;                        /* Возвращаем индекс разделения */
}

/*
 * Быстрая сортировка (рекурсивная версия)
 *
 * Параметры:
 *   arr[] - массив для сортировки
 *   low   - индекс первого элемента подмассива
 *   high  - индекс последнего элемента подмассива
 *
 * Алгоритм:
 * 1. Если подмассив содержит больше одного элемента
 * 2. Выполняем разбиение и получаем индекс разделения
 * 3. Рекурсивно сортируем левую часть
 * 4. Рекурсивно сортируем правую часть
 */
static void quick_sort(int arr[], int low, int high) {
    if (low < high) {                /* Базовый случай: если в подмассиве > 1 элемента */
        int pi = partition(arr, low, high);  /* Разбиваем и получаем индекс */
        quick_sort(arr, low, pi);            /* Сортируем левую часть [low..pi] */
        quick_sort(arr, pi + 1, high);       /* Сортируем правую часть [pi+1..high] */
    }
}

/*
 * Обёртка для быстрой сортировки
 *
 * Параметры:
 *   arr[] - массив для сортировки
 *   n     - количество элементов в массиве
 *
 * Упрощает вызов: не нужно передавать начальные индексы
 */
void quick_sort_wrapper(int arr[], int n) {
    if (n <= 1) return;              /* Если массив пустой или из 1 элемента - ничего не делаем */
    quick_sort(arr, 0, n - 1);       /* Вызываем рекурсивную сортировку */
}

/*
 * ============================================================================
 *                    ЧАСТЬ 2: СОРТИРОВКА СЛИЯНИЕМ (MERGE SORT)
 * ============================================================================
 *
 * Сортировка слиянием - устойчивый алгоритм сортировки.
 * Сложность: O(n log n) во всех случаях.
 *
 * Принцип работы:
 * 1. Массив рекурсивно делится на две половины
 * 2. Каждая половина сортируется
 * 3. Две отсортированные половины сливаются в один массив
 *
 * Преимущества:
 * - Гарантированная сложность O(n log n)
 * - Устойчивая сортировка (сохраняет порядок равных элементов)
 * - Хороша для связанных списков
 *
 * Недостатки:
 * - Требует дополнительной памяти O(n)
 * ============================================================================
 */

 /*
  * Слияние двух отсортированных подмассивов
  *
  * Параметры:
  *   arr[]  - массив, содержащий два отсортированных подмассива
  *   left   - индекс начала левого подмассива
  *   mid    - индекс конца левого подмассива (и середина)
  *   right  - индекс конца правого подмассива
  *
  * Подмассивы: arr[left..mid] и arr[mid+1..right] уже отсортированы
  * Результат: arr[left..right] отсортирован
  *
  * Алгоритм:
  * 1. Создаём временные массивы для левой и правой половин
  * 2. Копируем данные во временные массивы
  * 3. Сливаем два массива, выбирая меньший элемент на каждом шаге
  * 4. Копируем оставшиеся элементы из обоих массивов
  * 5. Освобождаем временные массивы
  */
static void merge(int arr[], int left, int mid, int right) {
    int n1 = mid - left + 1;         /* Размер левой половины */
    int n2 = right - mid;            /* Размер правой половины */
    int* L = (int*)malloc(n1 * sizeof(int));  /* Временный массив для левой половины */
    int* R = (int*)malloc(n2 * sizeof(int));  /* Временный массив для правой половины */
    int i, j, k;                     /* Счётчики для обхода массивов */

    /* Копируем данные из левой половины во временный массив L */
    for (i = 0; i < n1; i++) {
        L[i] = arr[left + i];
    }

    /* Копируем данные из правой половины во временный массив R */
    for (j = 0; j < n2; j++) {
        R[j] = arr[mid + 1 + j];
    }

    i = 0;                           /* Индекс для левого массива */
    j = 0;                           /* Индекс для правого массива */
    k = left;                        /* Индекс для результирующего массива */

    /* Основной цикл слияния: сравниваем элементы из L и R */
    while (i < n1 && j < n2) {
        if (L[i] <= R[j]) {          /* Если элемент из левой половины меньше или равен */
            arr[k] = L[i];           /* Берём его из левой половины */
            i++;                     /* Переходим к следующему элементу в левой половине */
        }
        else {                     /* Иначе элемент из правой половины меньше */
            arr[k] = R[j];           /* Берём его из правой половины */
            j++;                     /* Переходим к следующему элементу в правой половине */
        }
        k++;                         /* Переходим к следующей позиции в результате */
    }

    /* Копируем оставшиеся элементы из левой половины (если есть) */
    while (i < n1) {
        arr[k] = L[i];
        i++;
        k++;
    }

    /* Копируем оставшиеся элементы из правой половины (если есть) */
    while (j < n2) {
        arr[k] = R[j];
        j++;
        k++;
    }

    /* Освобождаем временные массивы */
    free(L);
    free(R);
}

/*
 * Сортировка слиянием (рекурсивная версия)
 *
 * Параметры:
 *   arr[]  - массив для сортировки
 *   left   - индекс первого элемента подмассива
 *   right  - индекс последнего элемента подмассива
 *
 * Алгоритм:
 * 1. Если подмассив содержит больше одного элемента
 * 2. Находим середину и делим массив на две части
 * 3. Рекурсивно сортируем левую половину
 * 4. Рекурсивно сортируем правую половину
 * 5. Сливаем две отсортированные половины
 */
static void merge_sort(int arr[], int left, int right) {
    if (left < right) {              /* Базовый случай: если в подмассиве > 1 элемента */
        int mid = left + (right - left) / 2;  /* Находим середину (без переполнения) */
        merge_sort(arr, left, mid);           /* Сортируем левую половину */
        merge_sort(arr, mid + 1, right);      /* Сортируем правую половину */
        merge(arr, left, mid, right);         /* Сливаем две отсортированные половины */
    }
}

/*
 * Обёртка для сортировки слиянием
 *
 * Параметры:
 *   arr[] - массив для сортировки
 *   n     - количество элементов в массиве
 */
void merge_sort_wrapper(int arr[], int n) {
    if (n <= 1) return;              /* Если массив пустой или из 1 элемента - ничего не делаем */
    merge_sort(arr, 0, n - 1);       /* Вызываем рекурсивную сортировку */
}


/*
 * Слияние двух отсортированных подмассивов (без выделения памяти)
 * Использует переданный временный массив temp[]
 */
static void merge_fast(int arr[], int left, int mid, int right, int temp[]) {
    int i = left;        /* Индекс для левой половины */
    int j = mid + 1;     /* Индекс для правой половины */
    int k = left;        /* Индекс для временного массива */

    /* Сливаем две половины во временный массив */
    while (i <= mid && j <= right) {
        if (arr[i] <= arr[j]) {
            temp[k] = arr[i];
            i++;
        }
        else {
            temp[k] = arr[j];
            j++;
        }
        k++;
    }

    /* Копируем оставшиеся элементы из левой половины */
    while (i <= mid) {
        temp[k] = arr[i];
        i++;
        k++;
    }

    /* Копируем оставшиеся элементы из правой половины */
    while (j <= right) {
        temp[k] = arr[j];
        j++;
        k++;
    }

    /* Копируем обратно из временного массива в исходный */
    for (i = left; i <= right; i++) {
        arr[i] = temp[i];
    }
}

/*
 * ============================================================================
 *               ЧАСТЬ 3: ВНЕШНЯЯ СОРТИРОВКА (EXTERNAL SORT)
 * ============================================================================
 *
 * Внешняя сортировка используется, когда данные не помещаются в оперативную память.
 * Данные хранятся на диске и читаются по частям (буферами).
 *
 * Алгоритм:
 * 1. Фаза сортировки: данные разбиваются на сегменты, каждый сегмент
 *    сортируется в памяти и записывается на диск.
 * 2. Фаза слияния: выполняется k-путевое слияние сегментов с использованием
 *    буферов (читаем по частям, не загружая всё в память).
 *
 * Преимущества:
 * - Позволяет сортировать данные любого размера
 * - Использует минимальное количество оперативной памяти
 * - Эффективно работает с дисковыми операциями ввода-вывода
 * ============================================================================
 */

 /*
  * Структура для хранения информации о сегменте (run) при внешней сортировке
  *
  * Сегмент — это отсортированная часть данных, сохранённая на диске
  *
  * Поля:
  *   data    - указатель на массив данных сегмента в памяти
  *   size    - количество элементов в сегменте
  *   current - текущий индекс для чтения (указывает, какой элемент следующий)
  */
typedef struct {
    int* data;                       /* Указатель на массив данных сегмента */
    int size;                        /* Количество элементов в сегменте */
    int current;                     /* Текущий индекс для чтения */
} RunInfo;

/*
 * Структура для чтения сегмента по частям (без загрузки всего сегмента в память)
 *
 * В реальной внешней сортировке сегменты могут быть огромными (гигабайты)
 * и не помещаться в память целиком. Поэтому мы читаем их по частям,
 * используя буфер фиксированного размера.
 *
 * Поля:
 *   file          - указатель на открытый файл сегмента
 *   buffer        - буфер для хранения текущего блока данных
 *   buffer_size   - размер буфера (сколько элементов помещается)
 *   buffer_count  - сколько элементов реально загружено в буфер
 *   current_pos   - текущая позиция в буфере
 *   is_empty      - флаг: закончились ли данные в сегменте (1 - да)
 */
typedef struct {
    FILE* file;                      /* Указатель на открытый файл сегмента */
    int* buffer;                     /* Буфер для хранения текущего блока данных */
    int buffer_size;                 /* Размер буфера (сколько элементов помещается) */
    int buffer_count;                /* Сколько элементов реально загружено в буфер */
    int current_pos;                 /* Текущая позиция в буфере */
    int is_empty;                    /* Флаг: закончились ли данные в сегменте (1 - да) */
} RunReader;

/*
 * Создание временного файла для хранения сегмента
 *
 * Параметры:
 *   run_num - номер сегмента (для уникальности имени)
 *
 * Возвращает: имя созданного файла
 *
 * Каждый сегмент сохраняется в отдельный временный файл.
 * Имя файла: temp_run_N.dat, где N - номер сегмента.
 */
static char* create_temp_filename(int run_num) {
    char* filename = (char*)malloc(32 * sizeof(char));  /* Выделяем память для имени файла */
    sprintf(filename, "temp_run_%d.dat", run_num);      /* Форматируем строку с именем */
    return filename;                                     /* Возвращаем имя файла */
}

/*
 * Запись сегмента во временный файл
 *
 * Параметры:
 *   arr      - отсортированный массив (сегмент)
 *   size     - размер массива
 *   filename - имя файла для сохранения
 *
 * Сохраняет отсортированный массив на диск в бинарном формате.
 * Используется бинарный режим для эффективной записи целых чисел.
 */
static void write_run_to_file(int arr[], int size, const char* filename) {
    FILE* file = fopen(filename, "wb");  /* Открываем файл для записи в бинарном режиме */
    if (!file) {                         /* Проверяем, удалось ли открыть файл */
        perror("Не удалось создать временный файл");  /* Выводим ошибку */
        return;                                      /* Выходим из функции */
    }
    fwrite(arr, sizeof(int), size, file); /* Записываем массив в файл (sizeof(int) байт на элемент) */
    fclose(file);                         /* Закрываем файл (освобождаем ресурсы) */
}

/*
 * Создание читателя для сегмента
 *
 * Параметры:
 *   filename    - имя файла сегмента
 *   buffer_size - размер буфера (сколько элементов читать за раз)
 *
 * Возвращает: указатель на созданного читателя или NULL при ошибке
 *
 * ВАЖНО: сегмент НЕ загружается в память целиком!
 * Вместо этого мы открываем файл и будем читать из него блоками
 * (по buffer_size элементов за раз).
 *
 * Алгоритм:
 * 1. Выделяем память под структуру читателя
 * 2. Открываем файл для чтения в бинарном режиме
 * 3. Выделяем память под буфер
 * 4. Загружаем первую порцию данных в буфер
 * 5. Инициализируем все поля структуры
 */
static RunReader* create_run_reader(const char* filename, int buffer_size) {
    RunReader* reader = (RunReader*)malloc(sizeof(RunReader));  /* Выделяем память под читателя */
    if (!reader) return NULL;                                    /* Проверка выделения */

    reader->file = fopen(filename, "rb");  /* Открываем файл для чтения в бинарном режиме */
    if (!reader->file) {                   /* Проверяем, удалось ли открыть файл */
        free(reader);                      /* Освобождаем память читателя */
        return NULL;                       /* Возвращаем NULL */
    }

    reader->buffer = (int*)malloc(buffer_size * sizeof(int));  /* Выделяем память под буфер */
    if (!reader->buffer) {                 /* Проверка выделения */
        fclose(reader->file);              /* Закрываем файл */
        free(reader);                      /* Освобождаем память читателя */
        return NULL;                       /* Возвращаем NULL */
    }

    /* Инициализируем все поля структуры */
    reader->buffer_size = buffer_size;     /* Сохраняем размер буфера */
    reader->buffer_count = 0;              /* Пока ничего не загружено в буфер */
    reader->current_pos = 0;               /* Начинаем с позиции 0 */
    reader->is_empty = 0;                  /* Вначале считаем, что данные есть */

    /* Загружаем первую порцию данных из файла в буфер */
    reader->buffer_count = fread(reader->buffer, sizeof(int), buffer_size, reader->file);
    reader->current_pos = 0;               /* Начинаем с первого элемента буфера */

    /* Если ничего не прочитали, значит файл пустой или закончился */
    if (reader->buffer_count == 0) {
        reader->is_empty = 1;              /* Отмечаем, что данные закончились */
    }

    return reader;                         /* Возвращаем созданного читателя */
}

/*
 * Получение следующего элемента из сегмента
 *
 * Параметры:
 *   reader   - указатель на читателя сегмента
 *   out_value - указатель для сохранения прочитанного значения
 *
 * Возвращает: 1 если элемент получен, 0 если данных больше нет
 *
 * Алгоритм:
 * 1. Если сегмент пуст или данные закончились - возвращаем 0
 * 2. Если достигли конца буфера - загружаем следующую порцию из файла
 * 3. Если ничего не прочитали - данные закончились
 * 4. Возвращаем текущий элемент и сдвигаем позицию
 *
 * Важно: это потоковое чтение - данные читаются по частям,
 * а не загружаются все сразу в память.
 */
static int get_next_from_reader(RunReader* reader, int* out_value) {
    if (reader->is_empty) return 0;   /* Если сегмент пуст или данные закончились - выходим */

    /* Проверяем, достигли ли конца текущего буфера */
    if (reader->current_pos >= reader->buffer_count) {
        /* Загружаем следующую порцию из файла */
        reader->buffer_count = fread(reader->buffer, sizeof(int), reader->buffer_size, reader->file);
        reader->current_pos = 0;               /* Начинаем с начала буфера */

        /* Если ничего не прочитали - данные закончились */
        if (reader->buffer_count == 0) {
            reader->is_empty = 1;              /* Отмечаем, что данные закончились */
            return 0;                          /* Возвращаем 0 (нет данных) */
        }
    }

    /* Сохраняем текущий элемент в out_value */
    *out_value = reader->buffer[reader->current_pos];
    reader->current_pos++;               /* Перемещаем позицию на следующий элемент */
    return 1;                            /* Возвращаем 1 (данные есть) */
}

/*
 * Уничтожение читателя (закрытие файла и освобождение памяти)
 *
 * Параметры:
 *   reader - указатель на читателя для уничтожения
 *
 * Освобождает все ресурсы, связанные с читателем:
 * - Закрывает файл
 * - Освобождает буфер
 * - Освобождает структуру читателя
 */
static void destroy_run_reader(RunReader* reader) {
    if (!reader) return;                 /* Если читатель NULL - ничего не делаем */
    if (reader->file) fclose(reader->file);  /* Закрываем файл (если открыт) */
    if (reader->buffer) free(reader->buffer);  /* Освобождаем буфер (если выделен) */
    free(reader);                         /* Освобождаем структуру читателя */
}

/*
 * Внешняя сортировка слиянием с использованием буферов (основная функция)
 *
 * Параметры:
 *   input        - массив для сортировки
 *   n            - размер массива
 *   segment_size - размер сегмента (сколько элементов помещается в память)
 *   buffer_size  - размер буфера для чтения (сколько элементов читается за раз)
 *
 * Возвращает: отсортированный массив или NULL при ошибке
 *
 * Алгоритм:
 * 1. Фаза сортировки: разбиваем данные на сегменты, сортируем каждый,
 *    записываем на диск (создаём временные файлы)
 * 2. Фаза слияния: открываем все файлы сегментов, НО НЕ ЗАГРУЖАЕМ ИХ В ПАМЯТЬ,
 *    читаем данные по частям (буферами) и выполняем k-путевое слияние
 * 3. Фаза очистки: удаляем временные файлы и освобождаем память
 *
 * Преимущества:
 * - В памяти хранятся только буферы (небольшие части сегментов)
 * - Можно сортировать данные любого размера
 * - Эффективно использует операции ввода-вывода
 */
int* external_merge_sort_with_buffer(int input[], int n, int segment_size, int buffer_size) {
    int num_segments;                  /* Количество сегментов */
    int i, k;                          /* Счётчики для циклов */
    int* result;                       /* Результирующий массив */
    char** filenames;                  /* Массив имён временных файлов */
    RunReader** readers;               /* Массив читателей для каждого сегмента */
    int* current_values;               /* Текущие значения из каждого сегмента */
    int* has_values;                   /* Флаг: есть ли значение в сегменте (1 - есть, 0 - нет) */

    /* Проверка корректности входных данных */
    if (n <= 0 || segment_size <= 0 || buffer_size <= 0) {
        return NULL;                   /* Некорректные параметры - возвращаем NULL */
    }

    /* Вычисляем количество сегментов (округляем вверх) */
    num_segments = (n + segment_size - 1) / segment_size;

    printf("Внешняя сортировка с буферами: %d элементов, сегментов: %d\n", n, num_segments);

    /* ============================================================
     * ФАЗА 1: СОРТИРОВКА СЕГМЕНТОВ И ЗАПИСЬ НА ДИСК
     * ============================================================ */

     /* Выделяем память под массив имён файлов для каждого сегмента */
    filenames = (char**)malloc(num_segments * sizeof(char*));
    if (!filenames) return NULL;       /* Ошибка выделения памяти */

    printf("Фаза 1: Сортировка сегментов...\n");

    /* Обрабатываем каждый сегмент по очереди */
    for (i = 0; i < num_segments; i++) {
        int start = i * segment_size;            /* Начало сегмента в исходном массиве */
        int end = (i + 1) * segment_size;        /* Конец сегмента в исходном массиве */
        if (end > n) end = n;                    /* Последний сегмент может быть меньше */
        int current_size = end - start;          /* Реальный размер текущего сегмента */

        /* Шаг 1: Извлекаем данные из входного массива в сегмент */
        int* segment = (int*)malloc(current_size * sizeof(int));
        for (int j = 0; j < current_size; j++) {
            segment[j] = input[start + j];       /* Копируем данные из исходного массива */
        }

        /* Шаг 2: Сортируем сегмент в памяти (быстрой сортировкой) */
        quick_sort_wrapper(segment, current_size);

        /* Шаг 3: Записываем отсортированный сегмент на диск */
        filenames[i] = create_temp_filename(i);   /* Создаём уникальное имя файла */
        write_run_to_file(segment, current_size, filenames[i]);  /* Записываем на диск */
        printf("  Сегмент %d: размер %d\n", i, current_size);

        free(segment);                     /* Освобождаем память (данные уже на диске) */
    }

    /* ============================================================
     * ФАЗА 2: СЛИЯНИЕ С ПОШАГОВЫМ ЧТЕНИЕМ (БЕЗ ЗАГРУЗКИ ВСЕХ В ПАМЯТЬ)
     * ============================================================ */

    printf("Фаза 2: Слияние сегментов...\n");

    /*
     * ВАЖНО: СЕГМЕНТЫ НЕ ЗАГРУЖАЮТСЯ В ПАМЯТЬ!
     * Вместо этого мы создаём читателей, которые будут
     * читать данные из файлов по частям (буферами)
     */

     /* Выделяем память под массив читателей */
    readers = (RunReader**)malloc(num_segments * sizeof(RunReader*));
    if (!readers) return NULL;

    /* Выделяем память для текущих значений и флагов наличия */
    current_values = (int*)malloc(num_segments * sizeof(int));
    has_values = (int*)malloc(num_segments * sizeof(int));

    if (!current_values || !has_values) return NULL;

    /* Создаём читателя для каждого сегмента (НЕ ЗАГРУЖАЕМ ВСЁ В ПАМЯТЬ!) */
    for (i = 0; i < num_segments; i++) {
        readers[i] = create_run_reader(filenames[i], buffer_size);
        if (!readers[i]) return NULL;          /* Ошибка создания читателя */

        /* Сразу читаем первый элемент из каждого сегмента */
        has_values[i] = get_next_from_reader(readers[i], &current_values[i]);
    }

    /* Создаём результирующий массив */
    result = (int*)malloc(n * sizeof(int));
    if (!result) return NULL;

    /*
     * Выполняем k-путевое слияние без загрузки всех сегментов в память
     *
     * Алгоритм:
     * 1. На каждом шаге ищем минимальный элемент среди всех сегментов
     * 2. Добавляем его в результат
     * 3. Читаем следующий элемент из того же сегмента
     * 4. Повторяем пока все сегменты не будут прочитаны
     *
     * Преимущество: в памяти находятся только буферы (по buffer_size элементов),
     * а не все сегменты целиком.
     */
    k = 0;                                 /* Индекс в результирующем массиве */

    /* Пока есть элементы хотя бы в одном сегменте */
    while (k < n) {
        int min_val = INT_MAX;             /* Начальное значение для поиска минимума */
        int min_segment = -1;              /* Индекс сегмента с минимальным элементом */

        /* Шаг 1: Поиск минимального элемента среди всех сегментов */
        for (i = 0; i < num_segments; i++) {
            /* Проверяем, есть ли ещё элементы в этом сегменте */
            if (has_values[i] && current_values[i] < min_val) {
                min_val = current_values[i];   /* Запоминаем минимальное значение */
                min_segment = i;               /* Запоминаем, из какого сегмента */
            }
        }

        /* Если не нашли ни одного элемента - выходим */
        if (min_segment == -1) break;

        /* Шаг 2: Добавляем минимальный элемент в результат */
        result[k] = min_val;
        k++;

        /* Читаем следующий элемент из того же сегмента */
        has_values[min_segment] = get_next_from_reader(readers[min_segment],
            &current_values[min_segment]);
    }

    /* ============================================================
     * ФАЗА 3: ОЧИСТКА ВРЕМЕННЫХ ФАЙЛОВ
     * ============================================================ */

    printf("Очистка временных файлов...\n");

    /* Удаляем все временные файлы и освобождаем память */
    for (i = 0; i < num_segments; i++) {
        remove(filenames[i]);              /* Удаляем файл с диска */
        free(filenames[i]);                /* Освобождаем имя файла */
        destroy_run_reader(readers[i]);    /* Уничтожаем читателя (закрываем файл) */
    }

    /* Освобождаем все выделенные массивы */
    free(filenames);                       /* Освобождаем массив имён файлов */
    free(readers);                         /* Освобождаем массив читателей */
    free(current_values);                  /* Освобождаем массив текущих значений */
    free(has_values);                      /* Освобождаем массив флагов */

    return result;                         /* Возвращаем отсортированный массив */
}

/*
 * ============================================================================
 *              ЧАСТЬ 4: ПАРАЛЛЕЛЬНАЯ СОРТИРОВКА СЛИЯНИЕМ
 * ============================================================================
 *
 * Параллельная сортировка слиянием использует несколько потоков
 * для ускорения сортировки на многоядерных системах.
 *
 * Принцип работы:
 * 1. Массив рекурсивно делится на части
 * 2. Каждая часть сортируется в отдельном потоке
 * 3. Затем выполняется слияние отсортированных частей
 *
 * Преимущества:
 * - Ускорение на многоядерных системах
 * - Сохраняет устойчивость сортировки слиянием
 *
 * Ограничения:
 * - Требует поддержки многопоточности
 * - Накладные расходы на создание потоков
 * ============================================================================
 */

 /*
  * Структура для передачи данных в поток при параллельной сортировке
  *
  * Поля:
  *   arr       - указатель на массив для сортировки
  *   left      - левая граница подмассива
  *   right     - правая граница подмассива
  *   depth     - текущая глубина рекурсии (для ограничения числа потоков)
  *   max_depth - максимальная глубина рекурсии (ограничивает количество потоков)
  */
typedef struct {
    int* arr;                          /* Массив для сортировки */
    int* temp;                         /* Временный массив (один на все потоки) */
    int left;                          /* Левая граница */
    int right;                         /* Правая граница */
    int depth;                         /* Текущая глубина рекурсии */
    int max_depth;                     /* Максимальная глубина */
} ThreadData;

#ifdef _WIN32
/* ============================================================
 * РЕАЛИЗАЦИЯ ДЛЯ WINDOWS (использует WinAPI)
 * ============================================================ */

 /*
  * Потоковая функция для параллельной сортировки слиянием (Windows)
  *
  * Параметры:
  *   arg - указатель на структуру ThreadData с данными для сортировки
  *
  * Возвращает: 0 при успешном завершении
  *
  * Алгоритм:
  * 1. Если подмассив пустой или из 1 элемента - выходим
  * 2. Если достигли максимальной глубины - сортируем последовательно
  * 3. Иначе создаём два потока для сортировки половин
  * 4. Ожидаем завершения потоков
  * 5. Сливаем две отсортированные половины
  */
static unsigned int __stdcall parallel_merge_sort_thread(void* arg) {
    ThreadData* data = (ThreadData*)arg;    /* Получаем данные из аргумента */
    int left = data->left;                  /* Левая граница */
    int right = data->right;                /* Правая граница */
    int depth = data->depth;                /* Текущая глубина */
    int max_depth = data->max_depth;        /* Максимальная глубина */

    if (left >= right) return 0;            /* Базовый случай: 0 или 1 элемент */

    int mid = left + (right - left) / 2;    /* Вычисляем середину */

    /* Проверяем, можно ли создавать новые потоки */
    if (depth < max_depth) {
        HANDLE threads[2];                  /* Массив дескрипторов потоков */
        ThreadData data_left, data_right;   /* Данные для левой и правой половин */

        /* Заполняем данные для левой половины */
        data_left.arr = data->arr;
        data_left.temp = data->temp;
        data_left.left = left;
        data_left.right = mid;
        data_left.depth = depth + 1;        /* Увеличиваем глубину */
        data_left.max_depth = max_depth;

        /* Заполняем данные для правой половины */
        data_right.arr = data->arr;
        data_right.temp = data->temp;
        data_right.left = mid + 1;
        data_right.right = right;
        data_right.depth = depth + 1;        /* Увеличиваем глубину */
        data_right.max_depth = max_depth;

        /* Создаём два потока для параллельной сортировки */
        threads[0] = (HANDLE)_beginthreadex(NULL, 0, parallel_merge_sort_thread, &data_left, 0, NULL);
        threads[1] = (HANDLE)_beginthreadex(NULL, 0, parallel_merge_sort_thread, &data_right, 0, NULL);

        /* Ожидаем завершения обоих потоков */
        WaitForMultipleObjects(2, threads, TRUE, INFINITE);

        /* Закрываем дескрипторы потоков (освобождаем ресурсы) */
        CloseHandle(threads[0]);
        CloseHandle(threads[1]);
    }
    else {
        /* Если достигли максимальной глубины - сортируем последовательно */
        ThreadData data_left = *data;        /* Копируем данные */
        data_left.left = left;               /* Устанавливаем левую границу */
        data_left.right = mid;               /* Устанавливаем правую границу */
        data_left.depth = depth + 1;         /* Увеличиваем глубину */

        ThreadData data_right = *data;       /* Копируем данные */
        data_right.left = mid + 1;           /* Устанавливаем левую границу */
        data_right.right = right;            /* Устанавливаем правую границу */
        data_right.depth = depth + 1;        /* Увеличиваем глубину */

        /* Последовательно сортируем обе половины */
        parallel_merge_sort_thread(&data_left);
        parallel_merge_sort_thread(&data_right);
    }

    /* Сливаем две отсортированные половины (используем функцию merge_fast ) */
    merge_fast(data->arr, left, mid, right, data->temp);
    return 0;                                /* Успешное завершение */
}

#endif

/*
 * Параллельная сортировка слиянием (обёртка)
 *
 * Параметры:
 *   arr[] - массив для сортировки
 *   n     - количество элементов в массиве
 *
 * Использует несколько потоков для ускорения сортировки.
 * Глубина рекурсии ограничена 3 (максимум 2^3 = 8 потоков).
 */
void parallel_merge_sort(int arr[], int n) {
    if (n <= 1) return;              /* Если массив пустой или из 1 элемента - ничего не делаем */

    /* Выделяем временный массив ОДИН РАЗ для всей сортировки */
    int* temp = (int*)malloc(n * sizeof(int));
    if (!temp) return;

    ThreadData data;                 /* Структура с данными для запуска */
    data.arr = arr;                  /* Передаём массив */
    data.temp = temp;
    data.left = 0;                   /* Начинаем с первого элемента */
    data.right = n - 1;              /* Заканчиваем последним элементом */
    data.depth = 0;                  /* Начинаем с глубины 0 */
    data.max_depth = 5;              /* Максимальная глубина = 3 (8 потоков) */

#ifdef _WIN32
    parallel_merge_sort_thread(&data);  /* Запускаем сортировку (Windows) */
#endif
    free(temp);
}

/*
 * ============================================================================
 *               ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ ДЛЯ ТЕСТОВ
 * ============================================================================
 */

 /*
  * Проверка, отсортирован ли массив
  *
  * Параметры:
  *   arr[] - массив для проверки
  *   n     - количество элементов в массиве
  *
  * Возвращает: 1 если массив отсортирован по возрастанию, 0 если нет
  *
  * Алгоритм:
  * Проходим по массиву и проверяем, что каждый следующий элемент
  * больше или равен предыдущему.
  */
int is_sorted(int arr[], int n) {
    if (n <= 1) return 1;              /* Пустой или из 1 элемента всегда отсортирован */
    for (int i = 0; i < n - 1; i++) {  /* Проходим по всем элементам кроме последнего */
        if (arr[i] > arr[i + 1]) {     /* Если текущий больше следующего */
            return 0;                  /* Нарушение порядка - массив не отсортирован */
        }
    }
    return 1;                          /* Все элементы в правильном порядке */
}

/*
 * Создание массива со случайными числами
 *
 * Параметры:
 *   n - количество элементов в массиве
 *
 * Возвращает: указатель на созданный массив или NULL при ошибке
 *
 * Создаёт массив из n случайных чисел в диапазоне 0-999
 * Используется для тестирования алгоритмов сортировки.
 */
int* create_random_array(int n) {
    if (n <= 0) return NULL;           /* Если размер не положительный - возвращаем NULL */
    int* arr = (int*)malloc(n * sizeof(int));  /* Выделяем память под массив */
    if (!arr) return NULL;             /* Проверка выделения */
    for (int i = 0; i < n; i++) {      /* Заполняем массив случайными числами */
        arr[i] = rand() % 1000;        /* Числа от 0 до 999 */
    }
    return arr;                        /* Возвращаем созданный массив */
}

/*
 * Копирование массива
 *
 * Параметры:
 *   arr[] - исходный массив для копирования
 *   n     - количество элементов в массиве
 *
 * Возвращает: указатель на новый массив с копией данных или NULL при ошибке
 *
 * Используется для создания копий массивов перед тестированием разных алгоритмов,
 * чтобы все алгоритмы работали с одинаковыми исходными данными.
 */
int* copy_array(int arr[], int n) {
    if (n <= 0 || arr == NULL) return NULL;  /* Проверка корректности входных данных */
    int* copy = (int*)malloc(n * sizeof(int));  /* Выделяем память под копию */
    if (!copy) return NULL;              /* Проверка выделения */
    memcpy(copy, arr, n * sizeof(int));  /* Копируем данные (побайтово) */
    return copy;                         /* Возвращаем копию массива */
}