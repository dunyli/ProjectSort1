/*
 * ============================================================================
 *                         ЧАСТЬ 1: БЫСТРАЯ СОРТИРОВКА (QUICKSORT)
 * ============================================================================
 */

 /*
  * Разбиение массива для быстрой сортировки
  * Использует стратегию с выбором среднего элемента в качестве опорного
  * и двухсторонним просмотром для улучшения производительности.
  *
  * Алгоритм:
  * 1. Выбираем опорный элемент как a[(l+r)/2]
  * 2. Двигаем указатель i слева направо, пока a[i] < опорный
  * 3. Двигаем указатель j справа налево, пока a[j] > опорный
  * 4. Если i >= j - останавливаемся
  * 5. Меняем местами a[i] и a[j], затем i++, j--
  * 6. Повторяем шаги 2-5 пока i <= j
  * 7. Возвращаем индекс j
  */
static int partition(int arr[], int low, int high) {
    int v = arr[(low + high) / 2];   /* Опорный элемент — средний */
    int i = low;                     /* Левый указатель */
    int j = high;                    /* Правый указатель */
    int temp;                        /* Временная переменная для обмена */

    while (i <= j) {
        /* Двигаем левый указатель вправо, пока элемент меньше опорного */
        while (arr[i] < v) {
            i++;
        }
        /* Двигаем правый указатель влево, пока элемент больше опорного */
        while (arr[j] > v) {
            j--;
        }
        /* Если указатели встретились, завершаем */
        if (i >= j) {
            break;
        }
        /* Меняем местами элементы и сдвигаем указатели */
        temp = arr[i];
        arr[i] = arr[j];
        arr[j] = temp;
        i++;
        j--;
    }

    return j;  /* Возвращаем индекс для разделения */
}

/*
 * Быстрая сортировка (рекурсивная версия)
 * Рекурсивно сортирует подмассив arr[low..high]
 * Использует улучшенное разбиение со средним элементом
 */
static void quick_sort(int arr[], int low, int high) {
    if (low < high) {
        int pi = partition(arr, low, high);  /* Находим индекс разделения */
        quick_sort(arr, low, pi);            /* Сортируем левую часть */
        quick_sort(arr, pi + 1, high);       /* Сортируем правую часть */
    }
}

/*
 * Обёртка для быстрой сортировки
 * Принимает массив и его размер, вызывает рекурсивную функцию
 */
void quick_sort_wrapper(int arr[], int n) {
    if (n <= 1) return;
    quick_sort(arr, 0, n - 1);
}



/*
 * ============================================================================
 *                    ЧАСТЬ 2: СОРТИРОВКА СЛИЯНИЕМ (MERGE SORT)
 * ============================================================================
 */

 /*
  * Слияние двух отсортированных подмассивов
  * arr[l..m] и arr[m+1..r] сливаются в один отсортированный массив
  * Используется временный массив для хранения результата
  */
static void merge(int arr[], int left, int mid, int right) {
    int n1 = mid - left + 1;   /* Размер левой половины */
    int n2 = right - mid;      /* Размер правой половины */
    int* L;                    /* Временный массив для левой половины */
    int* R;                    /* Временный массив для правой половины */
    int i, j, k;               /* Счётчики для обхода массивов */

    /* Выделяем память для временных массивов */
    L = (int*)malloc(n1 * sizeof(int));
    R = (int*)malloc(n2 * sizeof(int));

    /* Копируем данные во временные массивы */
    for (i = 0; i < n1; i++) {
        L[i] = arr[left + i];  /* Копируем левую половину */
    }
    for (j = 0; j < n2; j++) {
        R[j] = arr[mid + 1 + j]; /* Копируем правую половину */
    }

    i = 0;                     /* Индекс для левого массива */
    j = 0;                     /* Индекс для правого массива */
    k = left;                  /* Индекс для результирующего массива */

    /* Слияние: сравниваем элементы и вставляем меньший */
    while (i < n1 && j < n2) {
        if (L[i] <= R[j]) {    /* Если элемент из левой половины меньше */
            arr[k] = L[i];     /* Берём его */
            i++;               /* Переходим к следующему в левой */
        }
        else {               /* Иначе берём элемент из правой */
            arr[k] = R[j];
            j++;               /* Переходим к следующему в правой */
        }
        k++;                   /* Переходим к следующей позиции в результате */
    }

    /* Копируем оставшиеся элементы из левой половины (если есть) */
    while (i < n1) {
        arr[k] = L[i];
        i++;
        k++;
    }

    /* Копируем оставшиеся элементы из правой половины (если есть) */
    while (j < n2) {
        arr[k] = R[j];
        j++;
        k++;
    }

    /* Освобождаем временные массивы */
    free(L);
    free(R);
}

/*
 * Сортировка слиянием (рекурсивная версия)
 * Рекурсивно делит массив на две половины и сливает их
 */
static void merge_sort(int arr[], int left, int right) {
    if (left < right) {        /* Базовый случай: если подмассив из 1 элемента */
        int mid = left + (right - left) / 2; /* Находим середину */
        merge_sort(arr, left, mid);          /* Сортируем левую половину */
        merge_sort(arr, mid + 1, right);     /* Сортируем правую половину */
        merge(arr, left, mid, right);        /* Сливаем две половины */
    }
}

/*
 * Обёртка для сортировки слиянием
 * Принимает массив и его размер
 */
void merge_sort_wrapper(int arr[], int n) {
    if (n <= 1) return;
    merge_sort(arr, 0, n - 1);
}



/*
 * ============================================================================
 *               ЧАСТЬ 3: ВНЕШНЯЯ СОРТИРОВКА (EXTERNAL SORT)
 * ============================================================================
 */

 /*
  * Структура для хранения информации о сегменте (run) при внешней сортировке
  * Используется для k-путевого слияния
  *
  * Сегмент (run) — это отсортированная часть данных, сохранённая на диске
  * Каждый сегмент содержит:
  *   - data: указатель на массив данных сегмента в памяти
  *   - size: количество элементов в сегменте
  *   - current: текущий индекс для чтения (указывает, какой элемент следующий)
  */
typedef struct {
    int* data;                 /* Указатель на массив данных сегмента */
    int size;                  /* Количество элементов в сегменте */
    int current;               /* Текущий индекс для чтения */
} RunInfo;

/*
 * Создание временного файла для хранения сегмента
 * Каждый сегмент сохраняется в отдельный временный файл
 * Возвращает имя созданного файла
 */
static char* create_temp_filename(int run_num) {
    char* filename = (char*)malloc(32 * sizeof(char));
    sprintf(filename, "temp_run_%d.dat", run_num);
    return filename;
}

/*
 * Запись сегмента во временный файл
 * Сохраняет отсортированный массив на диск в бинарном формате
 *
 * Параметры:
 *   - arr: отсортированный массив (сегмент)
 *   - size: размер массива
 *   - filename: имя файла для сохранения
 */
static void write_run_to_file(int arr[], int size, const char* filename) {
    FILE* file = fopen(filename, "wb");  /* Открываем файл для записи в бинарном режиме */
    if (!file) {
        perror("Не удалось создать временный файл");
        return;
    }
    fwrite(arr, sizeof(int), size, file); /* Записываем массив в файл */
    fclose(file);                         /* Закрываем файл */
}

/*
 * Структура для чтения сегмента по частям (без загрузки всего сегмента в память)
 *
 * В реальной внешней сортировке сегменты могут быть огромными
 * и не помещаться в память целиком. Поэтому читаем их по частям
 * используя буфер фиксированного размера.
 */
typedef struct {
    FILE* file;                /* Указатель на открытый файл сегмента */
    int* buffer;               /* Буфер для хранения текущего блока данных */
    int buffer_size;           /* Размер буфера (сколько элементов помещается) */
    int buffer_count;          /* Сколько элементов реально загружено в буфер */
    int current_pos;           /* Текущая позиция в буфере */
    int segment_size;          /* Общий размер сегмента (для информации) */
    int elements_read;         /* Сколько элементов уже прочитано из сегмента */
    int is_empty;              /* Флаг: закончились ли данные в сегменте */
    int segment_index;         /* Индекс сегмента (для отладки) */
} RunReader;

/*
 * Создание читателя для сегмента
 * Открывает файл и инициализирует буфер
 *
 * Сегмент НЕ загружается в память целиком!
 * Вместо этого открываем файл и будем читать из него блоками
 */
static RunReader* create_run_reader(const char* filename, int buffer_size) {
    RunReader* reader = (RunReader*)malloc(sizeof(RunReader));
    if (!reader) return NULL;

    /* Открываем файл для чтения */
    reader->file = fopen(filename, "rb");
    if (!reader->file) {
        free(reader);
        perror("Не удалось открыть файл сегмента");
        return NULL;
    }

    /* Выделяем память под буфер */
    reader->buffer = (int*)malloc(buffer_size * sizeof(int));
    if (!reader->buffer) {
        fclose(reader->file);
        free(reader);
        return NULL;
    }

    /* Инициализируем все поля */
    reader->buffer_size = buffer_size;
    reader->buffer_count = 0;
    reader->current_pos = 0;
    reader->segment_size = 0;
    reader->elements_read = 0;
    reader->is_empty = 0;
    reader->segment_index = 0;

    /* Загружаем первую порцию данных в буфер */
    reader->buffer_count = fread(reader->buffer, sizeof(int), buffer_size, reader->file);
    reader->current_pos = 0;
    reader->elements_read = reader->buffer_count;

    /* Если ничего не прочитали - сегмент пуст */
    if (reader->buffer_count == 0) {
        reader->is_empty = 1;
    }

    return reader;
}

/*
 * Получение следующего элемента из сегмента
 * Если буфер закончился - загружаем следующую порцию из файла
 * Возвращает: 1 если элемент получен, 0 если данных больше нет
 * Значение элемента записывается в out_value
 */
static int get_next_from_reader(RunReader* reader, int* out_value) {
    /* Если сегмент пуст или данные закончились */
    if (reader->is_empty) {
        return 0;
    }

    /* Если достигли конца буфера - загружаем следующую порцию */
    if (reader->current_pos >= reader->buffer_count) {
        /* Читаем следующую порцию из файла */
        reader->buffer_count = fread(reader->buffer, sizeof(int), reader->buffer_size, reader->file);
        reader->current_pos = 0;

        /* Если ничего не прочитали - данные закончились */
        if (reader->buffer_count == 0) {
            reader->is_empty = 1;
            return 0;
        }

        /* Обновляем количество прочитанных элементов */
        reader->elements_read += reader->buffer_count;
    }

    /* Возвращаем текущий элемент и сдвигаем позицию */
    *out_value = reader->buffer[reader->current_pos];
    reader->current_pos++;
    return 1;
}

/*
 * Получение общего размера сегмента (без загрузки в память)
 * Определяем размер файла и вычисляем количество элементов
 */
static int get_segment_size_from_file(const char* filename) {
    FILE* file = fopen(filename, "rb");
    if (!file) return 0;

    fseek(file, 0, SEEK_END);
    long file_size = ftell(file);
    fclose(file);

    return file_size / sizeof(int);
}

/*
 * Уничтожение читателя (закрытие файла и освобождение памяти)
 */
static void destroy_run_reader(RunReader* reader) {
    if (!reader) return;
    if (reader->file) fclose(reader->file);
    if (reader->buffer) free(reader->buffer);
    free(reader);
}



/*
 * Внешняя сортировка слиянием (основная функция)
 *
 * Теория внешней сортировки:
 * ===========================
 * Внешняя сортировка используется, когда данные не помещаются в
 * оперативную память и хранятся на диске. Алгоритм состоит из двух фаз:
 *
 * Фаза 1: СОРТИРОВКА СЕГМЕНТОВ (Sorting Phase)
 * --------------------------------------------
 * 1. Данные разбиваются на сегменты (runs) размером, помещающимся в память
 * 2. Каждый сегмент сортируется в памяти (быстрой сортировкой)
 * 3. Отсортированные сегменты записываются на диск
 *
 * Фаза 2: СЛИЯНИЕ СЕГМЕНТОВ (Merge Phase - K-way Merge)
 * -----------------------------------------------------
 * 1. Открываются файлы всех сегментов (НО НЕ ЗАГРУЖАЮТСЯ В ПАМЯТЬ!)
 * 2. Из каждого файла читается первый элемент в буфер
 * 3. Выбирается минимальный элемент среди всех буферов
 * 4. Минимальный элемент добавляется в результат
 * 5. Из того же файла читается следующий элемент
 * 6. Процесс повторяется пока все файлы не будут прочитаны
 *
 * Преимущества:
 * - В памяти хранятся только буферы (небольшие части сегментов)
 * - Можно сортировать данные любого размера
 * - Эффективно использует операции ввода-вывода
 *
 * В данной реализации буфер хранит по одному элементу из каждого сегмента.
 * В реальных системах буферы обычно больше (несколько КБ или МБ) для
 * уменьшения количества операций чтения с диска.
 *
 * Параметры:
 *   - input: массив для сортировки
 *   - n: размер массива
 *   - segment_size: размер сегмента (сколько элементов помещается в память)
 *   - buffer_size: размер буфера для чтения (сколько элементов читается за раз)
 *
 * Возвращает: отсортированный массив
 */
int* external_merge_sort_streaming(int input[], int n, int segment_size, int buffer_size) {
    int num_segments;          /* Количество сегментов */
    int i, k;                  /* Счётчики */
    int* result;               /* Результирующий массив */
    char** filenames;          /* Массив имён временных файлов */
    RunReader** readers;       /* Массив читателей для каждого сегмента */
    int* current_values;       /* Текущие значения из каждого сегмента */
    int* has_values;           /* Флаг: есть ли значение в сегменте */

    /* Проверка корректности входных данных */
    if (n <= 0 || segment_size <= 0 || buffer_size <= 0) {
        printf("Ошибка: неверные параметры\n");
        return NULL;
    }

    /* Вычисляем количество сегментов (округляем вверх) */
    num_segments = (n + segment_size - 1) / segment_size;

    printf("Внешняя сортировка с потоковым чтением: %d элементов, сегментов: %d, буфер: %d\n",
        n, num_segments, buffer_size);

    /*
     * ФАЗА 1: СОРТИРОВКА СЕГМЕНТОВ И ЗАПИСЬ НА ДИСК
    */

    filenames = (char**)malloc(num_segments * sizeof(char*));
    if (!filenames) {
        printf("Ошибка выделения памяти для имён файлов\n");
        return NULL;
    }

    printf("Фаза 1: Сортировка сегментов...\n");

    /* Обрабатываем каждый сегмент по очереди */
    for (i = 0; i < num_segments; i++) {
        int start = i * segment_size;            /* Начало сегмента в исходном массиве */
        int end = (i + 1) * segment_size;        /* Конец сегмента в исходном массиве */
        if (end > n) end = n;                    /* Последний сегмент может быть меньше */
        int current_size = end - start;          /* Реальный размер текущего сегмента */

        /*
         * Шаг 1: Извлекаем данные из входного массива в сегмент
         * Создаём временный массив для текущего сегмента
         */
        int* segment = (int*)malloc(current_size * sizeof(int));
        for (int j = 0; j < current_size; j++) {
            segment[j] = input[start + j];       /* Копируем данные из исходного массива */
        }

        /*
         * Шаг 2: Сортируем сегмент в памяти
         * Используем быструю сортировку (эффективна для данных в памяти)
         */
        quick_sort_wrapper(segment, current_size);

        /*
         * Шаг 3: Записываем отсортированный сегмент на диск
         * Создаём уникальное имя файла для каждого сегмента
         */
        filenames[i] = create_temp_filename(i);
        write_run_to_file(segment, current_size, filenames[i]);
        printf("  Сегмент %d: размер %d, файл: %s\n", i, current_size, filenames[i]);

        /* Освобождаем память сегмента (данные уже на диске) */
        free(segment);
    }

    /* ============================================================
     * ФАЗА 2: СЛИЯНИЕ С ПОТОКОВЫМ ЧТЕНИЕМ (БЕЗ ЗАГРУЗКИ ВСЕХ В ПАМЯТЬ)
     * ============================================================ */

    printf("Фаза 2: Слияние сегментов (потоковое чтение)\n");

    /*
     * СЕГМЕНТЫ НЕ ЗАГРУЖАЮТСЯ В ПАМЯТЬ
     * Вместо этого создаём читателей, которые будут
     * читать данные из файлов по частям (буферами)
     */
    readers = (RunReader**)malloc(num_segments * sizeof(RunReader*));
    if (!readers) {
        printf("Ошибка выделения памяти для читателей\n");
        return NULL;
    }

    current_values = (int*)malloc(num_segments * sizeof(int));
    has_values = (int*)malloc(num_segments * sizeof(int));

    if (!current_values || !has_values) {
        printf("Ошибка выделения памяти\n");
        return NULL;
    }

    /*
     * Создаём читателя для каждого сегмента
     * Каждый читатель открывает файл и выделяет буфер
     */
    for (i = 0; i < num_segments; i++) {
        readers[i] = create_run_reader(filenames[i], buffer_size);
        if (!readers[i]) {
            printf("Ошибка создания читателя для сегмента %d\n", i);
            return NULL;
        }
        readers[i]->segment_index = i;

        /* Сразу читаем первый элемент из каждого сегмента */
        has_values[i] = get_next_from_reader(readers[i], &current_values[i]);
        printf("  Читатель %d создан, первый элемент: %s\n",
            i, has_values[i] ? "есть" : "нет");
    }

    /* Создаём результирующий массив */
    result = (int*)malloc(n * sizeof(int));
    if (!result) {
        printf("Ошибка выделения памяти для результата\n");
        return NULL;
    }

    /*
     * Выполняем k-путевое слияние без загрузки всех сегментов в память
     *
     * Алгоритм:
     * 1. На каждом шаге ищем минимальный элемент среди всех сегментов
     *    (читая значения из буферов, а не из всей памяти)
     * 2. Добавляем его в результат
     * 3. Читаем следующий элемент из того же сегмента
     * 4. Повторяем пока все сегменты не будут прочитаны
     *
     * Преимущество: в памяти находятся только буферы (по buffer_size элементов),
     * а не все сегменты целиком. Это позволяет сортировать данные любого размера.
     */
    k = 0;  /* Индекс в результирующем массиве */

    /* Пока есть элементы хотя бы в одном сегменте */
    while (k < n) {
        int min_val = INT_MAX;                  /* Начальное значение для поиска минимума */
        int min_segment = -1;                   /* Индекс сегмента с минимальным элементом */

        /*
         * Шаг 1: Поиск минимального элемента среди всех сегментов
         * Читаем значения из буферов, а не из всей памяти
         */
        for (i = 0; i < num_segments; i++) {
            /* Проверяем, есть ли ещё элементы в этом сегменте */
            if (has_values[i]) {
                if (current_values[i] < min_val) {
                    min_val = current_values[i];
                    min_segment = i;
                }
            }
        }

        /* Защита от ошибки: если не нашли ни одного элемента */
        if (min_segment == -1) {
            printf("Ошибка: не найден минимальный элемент\n");
            break;
        }

        /*
         * Шаг 2: Добавление минимального элемента в результат
         * И чтение следующего элемента из того же сегмента
         */
        result[k] = min_val;
        k++;

        /*
         * Читаем следующий элемент из сегмента, откуда взяли минимальный
         * Если элементы в сегменте закончились, has_values станет 0
         */
        has_values[min_segment] = get_next_from_reader(readers[min_segment],
            &current_values[min_segment]);
    }

    /* 
     * ФАЗА 3: ОЧИСТКА ВРЕМЕННЫХ ФАЙЛОВ  
     */

    printf("Очистка временных файлов...\n");

    /*
     * Удаляем все временные файлы с диска
     * Освобождаем память, выделенную под имена файлов и читателей
     */
    for (i = 0; i < num_segments; i++) {
        remove(filenames[i]);                  /* Удаляем файл с диска */
        free(filenames[i]);                    /* Освобождаем имя файла */
        destroy_run_reader(readers[i]);        /* Закрываем файл и освобождаем буфер */
    }

    free(filenames);                           /* Освобождаем массив имён файлов */
    free(readers);                             /* Освобождаем массив читателей */
    free(current_values);                      /* Освобождаем массив значений */
    free(has_values);                          /* Освобождаем массив флагов */

    printf("Внешняя сортировка с потоковым чтением завершена\n");

    return result;                             /* Возвращаем отсортированный массив */
}